class operator{
	public static void main(String[] args){
		int a = 10;
		int b= 20;
		boolean c = true;
		boolean d = false;
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(++a);
		System.out.println(--a);
		System.out.println(b-- + a--);
		System.out.println(a++ + b++);
		System.out.println(~a);
		System.out.println(~b);
		System.out.println(!c);
		System.out.println(!d);	
	}
}